-- ============================================
-- いいね削除(トグル)機能を有効化する SQL
-- ============================================
-- すでに Supabase に likes テーブルがある場合は、
-- このスクリプトを SQL Editor で実行してください
-- ============================================

-- 古い削除ポリシーを削除
DROP POLICY IF EXISTS "No one can delete likes" ON likes;

-- 新しい削除ポリシー: 誰でも自分がいいねしたものを削除できる
-- 注: IP アドレスベースなので、同じIPから削除可能
CREATE POLICY "Anyone can delete their own likes"
ON likes
FOR DELETE
USING (true);

-- ============================================
-- 完了！
-- ============================================
-- これで、いいねボタンを再度押すと削除できるようになります
-- ============================================
